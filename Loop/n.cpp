#include<bits/stdc++.h>
using namespace std;
int main() 
{
    char s;
    long long n;
    cin>>s>>n;
    for(int i=0;i<n;i++) 
    {
        long long x;
        cin>>x;
        for(int j=0;j<x;j++) 
        {
            cout<<s;
        }
        cout<<endl;
    }
}
